public class BigJavaWord {
    public static void main() {
        System.out.println("          J         A       V      V      A");
        System.out.println("          J       A   A      V    V      A  A");
        System.out.println("          J      A     A      V  V     A     A");
        System.out.println(" J        J     A A A A A      VV     A A A A A");
        System.out.println("  J JJJJJJ     A         A     V     A         A");
    }
}